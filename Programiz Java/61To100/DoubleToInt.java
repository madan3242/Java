// Java Program to convert double type variables to int
public class DoubleToInt {
    public static void main(String[] args) {
        double a = 23.78D;
        double b = 78.66D;

        int c = (int)a;
        int d = (int)b;

        System.out.println(c);
        System.out.println(d);
    }
}
